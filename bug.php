function foo(array $arr) {
  foreach ($arr as $key => $value) {
    if ($value === 'bar') {
      unset($arr[$key]);
    }
  }
  return $arr;
}

$arr = ['foo', 'bar', 'baz'];
$result = foo($arr);
print_r($result); // Output: Array ( [0] => foo [2] => baz )

//The bug is that foreach loop modifying the array during iteration can lead to unexpected results. When the element 'bar' is unset, the keys shift, causing the loop to skip elements.